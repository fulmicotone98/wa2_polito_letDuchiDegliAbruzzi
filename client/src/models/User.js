class User{
    username
    emailID
    password
    firstName
    lastName
    phoneNumber
    address
    constructor(username, emailID, password, firstName, lastName, phoneNumber, address) {
        this.username = username
        this.emailID = emailID
        this.password = password
        this.firstName = firstName
        this.lastName = lastName
        this.phoneNumber = phoneNumber
        this.address = address
    }
}
export default User